// Integral Numbers
let num1 = 10;
console.log(num1 + " is of type " + typeof num1);

// Decimal Numbers
let num2 = 3.142;
console.log(num2 + " is of type " + typeof num2);

// Special Numeric Types
console.log(25 / 0);
console.log(-25 / 0);
console.log(3 * "abc");

console.log(Number(false));
console.log(Number(true));
console.log(Number(""));
console.log(Number("5"));
